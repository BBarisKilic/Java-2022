public class Main {
    public static void main(String[] args) {

        // Byte type -> 1 Byte
        byte byteSayi = 127;
        System.out.println(byteSayi);

        // Short type -> 2 Bytes
        short shortSayi = 32767;
        System.out.println(shortSayi);

        // Integer type -> 4 bytes
        int sayi = 20;
        System.out.println(sayi);

        // Double type -> 8 bytes
        double doubleSayi = 2.3;
        System.out.println(doubleSayi);

        // Char type -> 2 bytes
        char karakter = 'A';
        System.out.println(karakter);

        // Boolean type -> 1 Byte
        boolean dogruMu = true;
        System.out.println(dogruMu);
    }
}